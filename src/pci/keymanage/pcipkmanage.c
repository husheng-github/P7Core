void pcipkmanage_getpkfilename(TERMINAL_PK_TYPE pktype,u8 *pkfilename)
{
    
	strcpy((s8 *)pkfilename,"/mtd2/");
	strcat((s8 *)pkfilename,(s8 *)&terminalfilename[pktype][0]);
}


s32 pcipkmanage_savepk(TERMINAL_PK_TYPE pktype,RSA_PUBLICKEY_INFO *publickeyinfo)
{
	u8 pkfilename[60];
	s32 ret;
	
	if(pktype > PK_PREVENT)
	{
		return PCI_RSAKEY_ERR;
	}
	
	pcipkmanage_getpkfilename(pktype,pkfilename);
	ret = pcicorecallfunc.pci_kern_write_file((const char *)pkfilename,(u8 *)publickeyinfo,sizeof(RSA_PUBLICKEY_INFO));
    #ifdef SXL_DEBUG
	dev_debug_printf("\r\npk save = %d, %d\r\n",ret, sizeof(RSA_PUBLICKEY_INFO));
	#endif
	if(ret != sizeof(RSA_PUBLICKEY_INFO))
	{
		return PCI_RSAKEY_ERR;
	}
	
	return PCI_PROCESSCMD_SUCCESS;
	
}


s32 pcipkmanage_readpk(TERMINAL_PK_TYPE pktype,RSA_PUBLICKEY_INFO *publickeyinfo)
{
	
	u8 pkfilename[60];
    s32 ret;
	u8 tmphashvalue[32];
	
	
	if(pktype >= PK_ACQUIRER&&pktype <= PK_ACQUIRER_ROOT)
	{        
		if(pktype == PK_PREVENT)
		{
			memcpy(publickeyinfo->IPKData,(u8 *)&gPreventpk[0],RSA2048PUBLICKEYDATALEN);
		}
		else if(pktype == PK_TRENDIT_ROOT)
		{
			memcpy(publickeyinfo->IPKData,(u8 *)&gPublickeyInfo[0],RSA2048PUBLICKEYDATALEN);
		}
		else if(pktype == PK_ACQUIRER_ROOT)
		{
			memcpy(publickeyinfo->IPKData,(u8 *)&gArAcquirerRootPublickeyInfo[0],RSA2048PUBLICKEYDATALEN);
		}
		else if(pktype == PK_ACQUIRER)
		{
			ret = dev_misc_machineparam_get(ACQUIRERPKDATA,publickeyinfo->IPKData, 292, PUBLICKEY_IPKDATA_OFFSET);
			if(ret != 0)
			{
                //如果参数中不存在acquirpkdata,则采用程序中固定的Acquirepk
                memcpy(publickeyinfo->IPKData,(u8 *)&gTrenditAcquirerPKInfo[0],RSA2048PUBLICKEYDATALEN);
				//return PCI_RSAKEY_ERR;
			}
		}
		return PCI_PROCESSCMD_SUCCESS;
		
	}
	else if(pktype >= PK_USBBOOT)
	{
		return PCI_RSAKEY_ERR;
	}
	
	pcipkmanage_getpkfilename(pktype,pkfilename);
	ret = pcicorecallfunc.pci_kern_read_file((const char *)pkfilename,(u8 *)publickeyinfo,sizeof(RSA_PUBLICKEY_INFO),0);
	if(ret == sizeof(RSA_PUBLICKEY_INFO))
	{
		
		pciarith_hash((u8 *)publickeyinfo,PUBLICKEY_CERTSIGN_OFFSET,tmphashvalue,HASHTYPE_256);
        #ifdef SXL_DEBUG
		dev_debug_printf("\r\npcipkmanage_readpk:\r\n");
		for(ret = 0;ret <32;ret++)
		{
			dev_debug_printf("%02x ",tmphashvalue[ret]);
		}
		dev_debug_printf("\r\n");
		for(ret = 0;ret <32;ret++)
		{
			dev_debug_printf("%02x ",publickeyinfo->CertSign[ret]);
		}
		dev_debug_printf("\r\n");
        #endif
		if(memcmp(publickeyinfo->CertSign,tmphashvalue,32) == 0)
    	{
    		return PCI_PROCESSCMD_SUCCESS;
    	}
		
	}
	
	return PCI_RSAKEY_ERR;
	
	
	
}

s32 pcipkmanage_readpkdata(TERMINAL_PK_TYPE pktype,u8 *pkdata)
{
	RSA_PUBLICKEY_INFO *publickeyinfo;
	s32 ret;

	publickeyinfo = (RSA_PUBLICKEY_INFO *)k_malloc(sizeof(RSA_PUBLICKEY_INFO));

	ret = pcipkmanage_readpk(pktype,publickeyinfo);
	if(ret == PCI_PROCESSCMD_SUCCESS)
	{
	    if(pkdata != NULL)
	    {
			memcpy(pkdata,publickeyinfo->IPKData,RSA2048PUBLICKEYDATALEN);
	    }
	}

	k_free(publickeyinfo);
	return ret;
}

void pcipkmanage_restoremanagepk(TERMINAL_PK_TYPE pktype)
{
	u8 key1status = 0,key2status = 0;
	u8 tmphashvalue[32];
    u8 tmphashvalue1[32];
	RSA_PUBLICKEY_INFO *publickeyinfo;
	s32 ret;
	
	
	memset(tmphashvalue,0,sizeof(tmphashvalue));
	memset(tmphashvalue1,0,sizeof(tmphashvalue1));
	
	
	publickeyinfo = (RSA_PUBLICKEY_INFO *)k_malloc(sizeof(RSA_PUBLICKEY_INFO));
	ret = pcipkmanage_readpk(pktype,publickeyinfo);
	if(ret == PCI_PROCESSCMD_SUCCESS)
	{
	    memcpy(tmphashvalue,publickeyinfo->CertSign,32);
		key1status = 1;
	}

	ret = pcipkmanage_readpk((TERMINAL_PK_TYPE)(pktype+1),publickeyinfo);
	if(ret == PCI_PROCESSCMD_SUCCESS)
	{
	    memcpy(tmphashvalue1,publickeyinfo->CertSign,32);
		key2status = 1;
	}

	
	if(key1status == 1)
    {
       if(memcmp(tmphashvalue1,tmphashvalue,32))
       {
	       pcipkmanage_readpk(pktype,publickeyinfo);
	       pcipkmanage_savepk((TERMINAL_PK_TYPE)(pktype+1),publickeyinfo); 
       }
        
    }
    else if((key1status == 0)&&(key2status == 1))
    {
		pcipkmanage_readpk((TERMINAL_PK_TYPE)(pktype+1),publickeyinfo);
		pcipkmanage_savepk(pktype,publickeyinfo); 
    }
    else
    {
        #ifdef SXL_DEBUG
        dev_debug_printf("%s(%d):%d key not exist\r\n", __FUNCTION__, __LINE__,pktype);
        #endif
    }
	
}


void pcipkmanage_checkmainpk(void)
{
	
	pcipkmanage_restoremanagepk(PK_ACQUIRER);
	
}



